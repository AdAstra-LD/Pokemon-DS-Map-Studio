    private void jbFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFinishActionPerformed
        if (tileMultiSelector.getNumTilesSelected() > 0) {
            returnValue = APPROVE_OPTION;
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "You need to select at least 1 tile", 
                    "No tiles selected", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbFinishActionPerformed

